package org.humanbooster.monprojet.model;

public class Country {

    public String name;
    public String nomCourt;
    public int indicatifTelephonique;


    public Country(String name, String nomCourt, int indicatifTelephonique) {
        this.name = name;
        this.nomCourt = nomCourt;
        this.indicatifTelephonique = indicatifTelephonique;
    }
}
